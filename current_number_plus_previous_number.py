# Set previous_num to 0
print('Printing current and previous number sum in a range(10)')
previous_num = 0

# Loop from 0 to 10
for i in range(10):
    sum = previous_num + i
    print('Current number is', i , 'Previous number is', previous_num , 'Sum:', sum)

# Modify previous_num
# Set previous_num to "i"
    previous_num = i